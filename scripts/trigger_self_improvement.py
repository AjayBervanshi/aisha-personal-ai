# ── Step 2: Get current main branch SHA ──────────────────────────────────────

def get_main_sha() -> str:
    """Return the current HEAD SHA of the main branch."""
    print("[2/6] Fetching main branch SHA...")
    resp = requests.get(f"{BASE_URL}/git/ref/heads/main", headers=GH_HEADERS, timeout=30)
    resp.raise_for_status()
    sha = resp.json()["object"]["sha"]
    print(f"    main SHA: {sha[:12]}...")
    return sha


# ── Step 3: Create branch ─────────────────────────────────────────────────────

def create_branch(branch_name: str, sha: str) -> bool:
    """Create a new branch from the given SHA. Returns True on success."""
    print(f"[3/6] Creating branch '{branch_name}'...")
    payload = {"ref": f"refs/heads/{branch_name}", "sha": sha}
    resp = requests.post(f"{BASE_URL}/git/refs", headers=GH_HEADERS, json=payload, timeout=30)
    if resp.status_code in (200, 201):
        print(f"    Branch created.")
        return True
    elif resp.status_code == 422:
        print(f"    Branch already exists — reusing.")
        return True
    else:
        print(f"    [ERROR] Branch creation failed: {resp.status_code} — {resp.text}")
        return False


# ── Step 4: Push file to branch ───────────────────────────────────────────────

def push_file_to_branch(
    branch_name: str, file_path: str, content: str, message: str
) -> bool:
    """Create or update a file on the given branch. Returns True on success."""
    print(f"[4/6] Pushing file '{file_path}' to branch...")

    encoded = base64.b64encode(content.encode("utf-8")).decode("utf-8")

    # Check if file already exists (to get its SHA for update)
    existing_sha = None
    check_resp = requests.get(
        f"{BASE_URL}/contents/{file_path}",
        headers=GH_HEADERS,
        params={"ref": branch_name},
        timeout=30,
    )
    if check_resp.status_code == 200:
        existing_sha = check_resp.json().get("sha")

    payload = {
        "message": message,
        "content": encoded,
        "branch": branch_name,
    }
    if existing_sha:
        payload["sha"] = existing_sha

    resp = requests.put(
        f"{BASE_URL}/contents/{file_path}",
        headers=GH_HEADERS,
        json=payload,
        timeout=30,
    )
    if resp.status_code in (200, 201):
        print(f"    File pushed successfully.")
        return True
    else:
        print(f"    [ERROR] File push failed: {resp.status_code} — {resp.text}")
        return False


# ── Step 5: Create PR ─────────────────────────────────────────────────────────

def create_pr(title: str, body: str, branch_name: str) -> tuple:
    """Create a PR from branch_name into main. Returns (pr_url, pr_number)."""
    print(f"[5a/6] Creating pull request...")
    payload = {
        "title": title,
        "body": body,
        "head": branch_name,
        "base": "main",
    }
    resp = requests.post(f"{BASE_URL}/pulls", headers=GH_HEADERS, json=payload, timeout=30)
    if resp.status_code == 201:
        data = resp.json()
        pr_url = data["html_url"]
        pr_number = data["number"]
        print(f"    PR created: {pr_url}")
        return pr_url, pr_number
    else:
        print(f"    [ERROR] PR creation failed: {resp.status_code} — {resp.text}")
        return None, None


# ── Step 6: Merge PR ──────────────────────────────────────────────────────────

def merge_pr(pr_number: int) -> bool:
    """Auto-merge the PR using GitHub API. Returns True on success."""
    print(f"[5b/6] Merging PR #{pr_number}...")

    # Wait briefly for GitHub to register the PR
    time.sleep(3)

    payload = {
        "commit_title": f"🧠 Auto-merge: Aisha self-improvement — {SKILL_NAME}",
        "commit_message": "Autonomously generated and merged by Aisha's self-improvement loop.",
        "merge_method": "squash",
    }
    resp = requests.put(
        f"{BASE_URL}/pulls/{pr_number}/merge",
        headers=GH_HEADERS,
        json=payload,
        timeout=30,
    )
    if resp.status_code == 200:
        print(f"    PR merged successfully.")
        return True
    else:
        print(f"    [WARN] PR merge failed: {resp.status_code} — {resp.text}")
        print(f"    (Branch and PR are still created — merge manually if needed)")
        return False


# ── Step 7: Trigger Render redeploy ──────────────────────────────────────────

def trigger_redeploy() -> bool:
    """Hit the Render deploy hook to trigger a fresh deployment."""
    print("[6a/6] Triggering Render redeploy...")
    try:
        resp = requests.post(RENDER_HOOK, timeout=30)
        if resp.status_code in (200, 201, 202):
            print(f"    Render deploy triggered.")
            return True
        else:
            print(f"    [WARN] Render hook returned {resp.status_code}: {resp.text[:200]}")
            return False
    except Exception as exc:
        print(f"    [WARN] Render trigger failed: {exc}")
        return False


# ── Step 8: Notify Ajay via Telegram ─────────────────────────────────────────

def notify_ajay(message: str) -> bool:
    """Send a Telegram message to Ajay. Returns True on success."""
    print("[6b/6] Sending Telegram notification to Ajay...")
    url = f"https://api.telegram.org/bot{BOT_TOKEN}/sendMessage"
    payload = {
        "chat_id": AJAY_ID,
        "text": message,
        "parse_mode": "Markdown",
    }
    try:
        resp = requests.post(url, json=payload, timeout=30)
        if resp.status_code == 200:
            print(f"    Telegram notification sent.")
            return True
        else:
            print(f"    [WARN] Telegram failed: {resp.status_code} — {resp.text[:200]}")
            return False
    except Exception as exc:
        print(f"    [WARN] Telegram notification failed: {exc}")
        return False


