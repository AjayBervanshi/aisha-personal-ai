def generate_skill_with_gemini(skill_name: str, description: str) -> str:
    """Ask Gemini to write a real, useful Python module for Aisha."""
    print(f"[1/6] Asking Gemini to generate {skill_name}...")

    prompt = f"""Write a complete, production-ready Python module called `{skill_name}` for an AI assistant named Aisha.

Description: {description}

Requirements:
- The file should be `src/skills/quote_engine.py`
- Define a class `QuoteEngine`
- Method `get_motivational_quote(self) -> str`: calls Gemini REST API to generate a fresh, beautiful Hindi motivational quote (in Devanagari script). Uses requests library. Gemini API key from env var `GEMINI_API_KEY`. Returns the quote string.
- Method `get_daily_quote(self) -> str`: calls `get_motivational_quote()` and formats the result for Telegram with a header emoji, the quote, and a footer attribution line in Hindi.
- At the bottom, a `if __name__ == "__main__":` block that creates a QuoteEngine instance and prints a sample quote.
- Uses ONLY stdlib + `requests` (no other third-party imports).
- Include a proper module docstring.
- All Hindi text must be in Devanagari script only (no Roman transliteration).
- Handle HTTP errors gracefully with a fallback hardcoded quote.
- Gemini endpoint: `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.0-flash:generateContent?key={{api_key}}`

Return ONLY the Python code with no markdown fences, no explanation, just raw Python."""

    url = f"https://generativelanguage.googleapis.com/v1beta/models/gemini-2.0-flash:generateContent?key={GEMINI_KEY}"
    payload = {
        "contents": [{"parts": [{"text": prompt}]}],
        "generationConfig": {"temperature": 0.7, "maxOutputTokens": 2048},
    }

    try:
        resp = requests.post(url, json=payload, timeout=60)
        resp.raise_for_status()
        data = resp.json()
        code = data["candidates"][0]["content"]["parts"][0]["text"].strip()

        # Strip markdown fences if Gemini added them anyway
        if code.startswith("```"):
            lines = code.splitlines()
            # Remove first and last fence lines
            start = 1 if lines[0].startswith("```") else 0
            end = len(lines) - 1 if lines[-1].strip() == "```" else len(lines)
            code = "\n".join(lines[start:end]).strip()

        print(f"    Generated {len(code)} chars of code from Gemini.")
        return code

    except Exception as exc:
        print(f"    [WARN] Gemini generation failed: {exc}. Using fallback template.")
        return _fallback_skill_code()


